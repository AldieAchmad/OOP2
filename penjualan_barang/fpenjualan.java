    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        // TODO add your handling code here:
        if (txt_nofaktur.getText().equals("") || txt_kodebarang.getText().equals("") || pilihbarang.getSelectedItem().equals("") || txt_hargasatuan.getText().equals("") || txt_jumlahjual.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "Moscraft House", JOptionPane.INFORMATION_MESSAGE);

        } else {
            String kdbarangg = txt_kodebarang.getText();
            String pilihbarangg = (String) pilihbarang.getSelectedItem();
            String hsatuann = txt_hargasatuan.getText();
            String tjumlahh = txt_jumlahjual.getText();
            String totall = txt_hitung.getText();

            try {
                Connection c = koneksi.getKoneksi();

                String sql = "INSERT INTO tbl_hitung_jual VALUES (?, ?, ?, ?, ?, ?)";

                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, null);
                p.setString(2, kdbarangg);
                p.setString(3, pilihbarangg);
                p.setString(4, hsatuann);
                p.setString(5, tjumlahh);
                p.setString(6, totall);

                p.executeUpdate();
                p.close();
            } catch (SQLException e) {
                System.out.println("Terjadi Error: " + e.getMessage());
            } finally {
                nofaktur();
                txt_kodebarang.setText("");
                pilihbarang.setSelectedItem("");
                txt_hargasatuan.setText("");
                txt_hitung.setText("");
                txt_jumlahjual.setText("");
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Moscraft House", JOptionPane.INFORMATION_MESSAGE);
                loadData();

            }
        }
    }//GEN-LAST:event_btn_tambahActionPerformed

